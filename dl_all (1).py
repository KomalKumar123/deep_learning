import numpy as np

X = np.array([[0,0],[0,1],[1,0],[1,1]])
y = np.array([0,0,0,1])

w = np.zeros(2)
b = 0
lr = 0.1

for _ in range(100):
    for i in range(len(X)):
        y_pred = 1 if np.dot(X[i],w)+b >= 0 else 0
        error = y[i] - y_pred
        w += lr * error * X[i]
        b += lr * error

pred = [1 if np.dot(x,w)+b>=0 else 0 for x in X]
acc = np.mean(pred == y) * 100

print("Weights:", w)
print("Bias:", b)
print("Error:", np.sum((y - pred)**2))
print("Accuracy:", acc, "%")

